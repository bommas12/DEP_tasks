/**
 * Calculates the negation of number
 * @param {number} number number you want to negate
 * @returns {number} opposite of the number(negation)
 * 
 * @example opposite(-7)
 */
function opposite(number) {
    return -number;
}

console.log(opposite(1)); //expected -1

console.log(opposite(0)); //expected -0
console.log(opposite(4.25)); //expected -4.25
console.log(opposite(-3.33)); //expected 3.33